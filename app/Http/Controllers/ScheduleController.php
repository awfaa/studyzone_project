     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $sched= new Sched();
        $sched->c_code=$request->c_code;
        $sched->c_day=$request->c_day;
        $sched->c_time=$request->c_time;
        $sched->class_name=$request->class_name;
        $sched->c_location=$request->c_location;
        $sched->created_at=today();
        $sched->updated_at=today();
        $sched->save();
        return redirect('add-schedule');
    }

